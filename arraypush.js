var x=[1,2,3,4,5]
console.log(x); //1,2,3,4,5
// add one more entry in array------>
// push will add one more entry
x.push(6);
console.log(x); //1,2,3,4,5,6
// remove last entry from array
// pop----> will remove last entry
x.pop();
console.log(x); //1,2,3,4,5 
